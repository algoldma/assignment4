<h2>Peer Grading</h2>

<p>This course can be very subjective due to the nature of "how does it look". To help remove biasness we utilize peer grading in this class. Each student will be assigned three people to grade for each assignment. You will be expected to review each students web site providing a number grade (0 to 100) along with feedback explaining what they did right and wrong. You will be given a grading rubric to go by however you also completed the assignment and should have a pretty good idea of what to expect. Your peer grading will be due within 48 hours of the assignment due date. You will be graded on the quality and fairness of your peer grading. For example if you just give everyone a 100 (when they do not deserve it, your grade will be lowered by 20%. Each assignment you will be assigned new people to grade. The goal is not have your grade the same person twice and of course not to grade someone in your group. Depending on the size of the class this may happen, please contact me if it does.</p><h2>Grading Policy:<a id="participation"></a><a id="classAssignments"></a></h2>
